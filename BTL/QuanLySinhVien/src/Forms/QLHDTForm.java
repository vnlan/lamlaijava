    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        try {
            // TODO add your handling code here:
            HeDT hedt = new HeDT();
            hedt.setMaHeDT(txtMaHeDT.getText());
            hedt.setTenHeDT(txtTenHeDT.getText());
            if(hedt.getMaHeDT().length()>0 && hedt.checkExist()==0){
                hedt.addHeDT();
            }else{
                hedt.updateHeDT();
            }
            JOptionPane.showMessageDialog(rootPane,"Cap nhat he dt thanh cong!");
        } catch (SQLException ex) {
            
            JOptionPane.showMessageDialog(rootPane,"That bai, moi nhap lai thong tin chinh xac!");
        }
        
    }//GEN-LAST:event_btnLuuActionPerformed

