    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            // TODO add your handling code here:
            HeDT hdt = new HeDT();
            hdt.setMaHeDT(txtMaHeDT.getText());
            
            if(hdt.checkExist()==1 && txtMaHeDT.getText().length()>0){
                hdt.deleteHeDT();
                JOptionPane.showMessageDialog(rootPane,"Xoá Hệ Đào Tạo thành công!");
            }else{
                JOptionPane.showMessageDialog(rootPane,"Hệ Đào Tạo không tồn tại để xóa!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Không thể Hệ Đào Tạo do ràng buộc!");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

